public class Basics {
    public static void main(String[] args) {

        // if condition 
        if(true) {
            System.out.println("Hello world");
        }
        // while loop 
        int count = 1;
        while (count != 5) {
            System.out.println(count);
            count ++;
        }

        // for loop 
        for(int i = 1; i != 5; i++) {
            System.out.println(i);
        }
    }
}
